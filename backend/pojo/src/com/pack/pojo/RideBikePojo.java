package com.pack.pojo;

public class RideBikePojo {
	private String hora;
	private Double latitude;
	private Double longitude;
	private Double velocidade;
	private Double distancia;
	private Double speed;
	private Double tempo_leitura;
	
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public Double getLatitude() {
		return latitude;
	}
	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}
	public Double getLongitude() {
		return longitude;
	}
	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}
	public Double getVelocidade() {
		return velocidade;
	}
	public void setVelocidade(Double velocidade) {
		this.velocidade = velocidade;
	}
	public Double getDistancia() {
		return distancia;
	}
	public void setDistancia(Double distancia) {
		this.distancia = distancia;
	}
	public Double getSpeed() {
		return speed;
	}
	public void setSpeed(Double speed) {
		this.speed = speed;
	}
	public Double getTempo_leitura() {
		return tempo_leitura;
	}
	public void setTempo_leitura(Double tempo_leitura) {
		this.tempo_leitura = tempo_leitura;
	}
	
}
